package com.intelligent.marking.bean;

/**
 * 获取省份
 */


public class ProvinceBean {
    /**
     * id : 省id
     * name : 省名称
     */

    private String id;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
